const fs = require('fs');
const FILE = 'DEV Skywide  Content (TEST v23).json';
const d = JSON.parse(fs.readFileSync(FILE, 'utf8'));
let updated = false;

d.nodes.forEach(n => {
    if (n.name.includes('Claude Draft') || n.name.includes('OpenAI Draft')) {
        let p = n.parameters;

        // The exact text we are looking to replace
        const oldFaqRule = "IMPORTANT: Do NOT generate a Frequently Asked Questions (FAQ) section. The FAQ section will be generated by a separate process. Focus only on the main article content.";
        const newFaqRule = "FAQ RULE: You must include an FAQ section at the end of the article. If the Creative Brief provides FAQs, you MUST use those exact questions. If the brief does NOT provide FAQs, formulate 3-5 of your own highly relevant FAQs based on the generated content.";

        const replaceFaqRule = (content) => {
            if (typeof content !== 'string') return content;
            return content.replace(oldFaqRule, newFaqRule);
        };

        if (p.text) {
            let fresh = replaceFaqRule(p.text);
            if (fresh !== p.text) {
                p.text = fresh;
                updated = true;
                console.log("✅ Updated FAQ rule in parameters.text for " + n.name);
            }
        }

        if (p.systemMessage) {
            let fresh = replaceFaqRule(p.systemMessage);
            if (fresh !== p.systemMessage) {
                p.systemMessage = fresh;
                updated = true;
                console.log("✅ Updated FAQ rule in parameters.systemMessage for " + n.name);
            }
        }

        if (p.messages && p.messages.values && p.messages.values.length > 0) {
            let old = p.messages.values[0].content;
            let fresh = replaceFaqRule(old);
            if (fresh !== old) {
                p.messages.values[0].content = fresh;
                updated = true;
                console.log("✅ Updated FAQ rule in parameters.messages.values for " + n.name);
            }
        }

        if (p.messages && p.messages.messageValues && p.messages.messageValues.length > 0) {
            let old = p.messages.messageValues[0].message;
            let fresh = replaceFaqRule(old);
            if (fresh !== old) {
                p.messages.messageValues[0].message = fresh;
                updated = true;
                console.log("✅ Updated FAQ rule in parameters.messages.messageValues for " + n.name);
            }
        }
    }
});

if (updated) {
    fs.writeFileSync(FILE, JSON.stringify(d, null, 2), 'utf8');
    console.log('\\n✅ Done! File saved.');
} else {
    console.log('No fixes needed, or the old FAQ string was not found.');
}
